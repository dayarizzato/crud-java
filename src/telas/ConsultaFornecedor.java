    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // alterar
        if(tabelaFornecedor.getSelectedRowCount()== 1){
            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setCodigo(Integer.parseInt((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 0)));
            fornecedor.setNome((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 1));
            fornecedor.setCnpj((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 2));
            fornecedor.setEndereco((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 3));
            fornecedor.setNumero((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 4));
            fornecedor.setBairro((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 5));
            fornecedor.setComplemento((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 6));
            fornecedor.setCep((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 7));
            fornecedor.setCidade((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 8));
            fornecedor.setEstado((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 9));
            fornecedor.setTelefone((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 10));
            fornecedor.setCelular((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 11));
            fornecedor.setEmail((String) tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 12));
            new CadastroFornecedor(fornecedor).setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um único registro para excluir.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // excluir
        if (tabelaFornecedor.getSelectedRow() == 1) {
            try {
                Connection bd = ConnectionFactory.getConection();
                FornecedorDAO fornecedorDAO = new FornecedorDAO(bd);
                Long codigo = Long.parseLong(tabelaFornecedor.getValueAt(tabelaFornecedor.getSelectedRow(), 0) + "");
                fornecedorDAO.excluir(codigo);
                
                ((DefaultTableModel) tabelaFornecedor.getModel()).removeRow(tabelaFornecedor.getSelectedRow());
                tabelaFornecedor.repaint();
                tabelaFornecedor.revalidate();
            } catch (SQLException ex) {
                Logger.getLogger(ConsultaFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione ao menos um registro para excluir!");
          
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        try {
            String nome = txtNome.getText();// 1- crio evento keyreleased para o cliente pesquisar 
            Connection bd = ConnectionFactory.getConection();

            FornecedorDAO fornecedorDao = new FornecedorDAO(bd);
            List<Fornecedor> lista = fornecedorDao.consultarFornecedor(nome);
            popularTabela(lista);
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtNomeKeyReleased

