    private void popularTabela(List<Fornecedor> lista) {
        tabelaFornecedor.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        DefaultTableModel modelo =(DefaultTableModel) tabelaFornecedor.getModel();
        modelo.setNumRows(0);
        
        lista.forEach( f ->{
            modelo.addRow(new String[] {f.getCodigo()+"", f.getNome(), f.getCnpj(), 
                f.getEndereco(), f.getNumero(), f.getBairro(), f.getComplemento(), f.getCep(), 
                f.getCidade(), f.getEstado(), f.getTelefone(), f.getCelular(), f.getEmail() });
        });
        
    }
