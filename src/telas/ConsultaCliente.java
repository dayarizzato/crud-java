    public void popularTabela(List<Cliente> lista) {
        tabelaCliente.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);//padrão
        DefaultTableModel modelo = (DefaultTableModel) tabelaCliente.getModel();
        modelo.setNumRows(0);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        lista.stream().forEach((Cliente c) -> {
            modelo.addRow(new String[]{c.getCodigo() + "", c.getNome(), c.getSexo().equals("M") ? "Masculino" : "Feminino", c.getEstadoCivil(), c.getEndereco(), 
                c.getComplemento(), c.getBairro(), c.getCep(), c.getCidade(), c.getEstado(), c.getNumero(),  c.getCpf(), c.getRg(),c.getTelefone(), c.getCelular(), c.getEmail(), 
                sdf.format(c.getDataNascimento()), sdf.format(c.getDataCadastro())});
        });
    }

