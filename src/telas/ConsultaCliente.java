    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        try {

            String nome = txtNome.getText();//capturando o que se escreve na tela

            Connection bd = ConnectionFactory.getConection();

            ClienteDAO clienteDao = new ClienteDAO(bd);
            List<Cliente> lista = clienteDao.consultarClientes(nome);
            popularTabela(lista);
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtNomeKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try {
            if(tabelaCliente.getSelectedRow()== 1){//selecione uma linha - selected row retorna a quantidade de linhas selecionado
                Connection bd = ConnectionFactory.getConection();
                ClienteDAO clienteDao = new ClienteDAO(bd);
                Long codigo = Long.parseLong(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 0) + "");
                clienteDao.excluir(codigo);

                ((DefaultTableModel) tabelaCliente.getModel()).removeRow(tabelaCliente.getSelectedRow());
                tabelaCliente.repaint();// recarrega tabela
                tabelaCliente.revalidate();
            }else{
                JOptionPane.showMessageDialog(null, "Selecione ao menos um registro para excluir!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

