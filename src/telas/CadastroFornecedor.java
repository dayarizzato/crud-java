    /**
     *
     * @param f
     * @return
     */
    public String validarCampos(Fornecedor f) {
        String msgRetorno = "";

        if (f.getNome().equals("")) {
            return "Nome é obrigatório!";
        }
        if (f.getCnpj().equals("")) {
            return "CNPJ é obrigatório!";
        }
        if (f.getEndereco().equals("")) {
            return "O endereço é obrigatório!";
        }
        if (f.getNumero().equals("")) {
            return "O número é obrigatório!";
        }
        if (f.getBairro().equals("")) {
            return "O bairro é obrigatório!";
        }
        if (f.getComplemento().equals("")) {
            return "O complemento é obrigatório!";
        }
        if (f.getCep().equals("")) {
            return "O cep é obrigatório!";
        }
        if (f.getCidade().equals("")) {
            return "A cidade é obrigatório!";
        }
        if (comboEstado.getSelectedItem().equals("Selecione:")) {
            return "O estado é obrigatório!";
        }
        if (f.getCelular().equals("")) {
            return "O celular é obrigatório!";
        }
        if (f.getEmail().equals("")) {
            return "O e-mail é obrigatório!";
        }
        return msgRetorno;

    }

