    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection bd = ConnectionFactory.getConection();
            FornecedorDAO fornecedorDAO = new FornecedorDAO(bd);
            if (fornecedor == null) {

                fornecedor = new Fornecedor();
                fornecedor.setNome(txtNome.getText());
                fornecedor.setCnpj(txtCnpj.getText());
                fornecedor.setEndereco(txtEndereco.getText());
                fornecedor.setNumero(txtNumero.getText());
                fornecedor.setBairro(txtBairro.getText());
                fornecedor.setComplemento(txtComplemento.getText());
                fornecedor.setCep(txtCep.getText());
                fornecedor.setCidade(txtCidade.getText());
                fornecedor.setEstado(comboEstado.getSelectedItem().toString());
                fornecedor.setTelefone(txtTelefone.getText());
                fornecedor.setCelular(txtCelular.getText());
                fornecedor.setEmail(txtEmail.getText());

                String msg = validarCampos(fornecedor);
                if (!msg.equals("")) {
                    JOptionPane.showMessageDialog(null, msg);
                    return;
                } else {
                    if (!ValidaCnpj.validaCnpj(fornecedor.getCnpj())) {
                        JOptionPane.showMessageDialog(null, "CNPJ inválido.");
                        return;
                    }
                }

                fornecedorDAO.inserirFornecedor(fornecedor);
                bd.close();

                JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso!");

                limparcampos();
            } else {
                // alteração
                fornecedor.setNome(txtNome.getText());
                fornecedor.setCnpj(txtCnpj.getText());
                fornecedor.setEndereco(txtEndereco.getText());
                fornecedor.setNumero(txtNumero.getText());
                fornecedor.setBairro(txtBairro.getText());
                fornecedor.setComplemento(txtComplemento.getText());
                fornecedor.setCep(txtCep.getText());
                fornecedor.setCidade(txtCidade.getText());
                fornecedor.setEstado(comboEstado.getSelectedItem().toString());
                fornecedor.setTelefone(txtTelefone.getText());
                fornecedor.setCelular(txtCelular.getText());
                fornecedor.setEmail(txtEmail.getText());
                fornecedorDAO.atualizar(fornecedor);
                JOptionPane.showMessageDialog(null, "Fornecedor atualizado com sucesso!");
                bd.close();
                new ConsultaFornecedor().setVisible(true);
                dispose();
            }

        } catch (SQLException ex) {
            Logger.getLogger(CadastroFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

